class Animal
{
	private String name;
	private int legs;

	public String getName()
	{
		return this.name;
	}

	public int getLegs()
	{
		return this.legs;
    }

    public void setLegs(int legs)
    {
        this.legs = legs;
    }

    public void setName(String name)
    {
        this.name = name;
    }
	
	public String toString()
	{
		return this.name;
	}
}
